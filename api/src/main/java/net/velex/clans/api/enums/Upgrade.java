package net.velex.clans.api.enums;

public enum Upgrade {
  /**
   * Indicates that there was an upgrade for the clan 'homes' section.
   */
  HOME_TYPE,
  /**
   * Indicates that there was an upgrade for the clan 'members' category, added members, leaves.
   */
  MEMBER_TYPE,
  /**
   * Indicates that there was an upgrade for the clan 'allies' section.
   */
  ALLY_TYPE
}
